// Program to illustrate use of increment and decrement operator
//author:Sebak Thapa

#include <stdio.h>

int main() {
    int num = 10;
    
    printf("The initial value is: %d\n" , num);
    
	//	incrementing the value and printing
	num++;
   	printf("The value after increment is: %d \n", num);
   	
   	//	decrementing the value and printing
	num--;
   	printf("The value after decrement is: %d", num);

    
    return 0;
}
