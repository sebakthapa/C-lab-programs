// Program to multiply two numbers (10&8) and display its sum
//author:Sebak Thapa

#include <stdio.h>

int main(){
	int a=10, b=8, product,sum;
	
	// calculating sum and product 
	sum= a + b;
	product = a * b;
	
	//	displaying the sum
	printf("The sum of %d and %d is %d \n", a,b,sum);
	printf("The product of %d and %d is %d", a,b,product);
	
	return 0;
}
